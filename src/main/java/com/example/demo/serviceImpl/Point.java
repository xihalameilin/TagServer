package com.example.demo.serviceImpl;

public class Point {
    /** the x value */
    public float x;

    /** the y value */
    public float y;

    /**
     * Constructor.
     *
     * @param x
     *            the x value
     * @param y
     *            the y value
     */
    public Point(float x, float y) {
        this.x = x;
        this.y = y;
    }
}
